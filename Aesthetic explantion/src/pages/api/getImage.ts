function getRandomImage() {
  if (returnedImages.length >= 80) {
    returnedImages = [];
  }

  let randomData;

  do {
    const randomIndex = Math.floor(Math.random() * imageNames.length);
    randomData = imageNames[randomIndex];
  } while (returnedImages.includes(randomData));

  return randomData;
}

